package org.firstinspires.ftc.teamcode.OpModes;

/*
the idea is that all the OpModes will change constants in here and then the "MatchMode" will
actually set all the motor values. This way, it's impossible to try and set the same motor to 2
different values
 */
public class DesiredMotorValues {
    public static double motorFTpower = 0.0;
    public static double motorFBpower = 0.0;
    public static double motorBTpower = 0.0;
    public static double motorBBpower = 0.0;
}
